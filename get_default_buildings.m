function Buildings = get_default_buildings()
% GET_DEFAULT_BUILDINGS  返回默认建筑列表（统一为 axis-aligned 矩形）
%
%   Buildings(k).xmin/xmax/ymin/ymax — 第 k 个矩形建筑边界
%   Buildings(k).material           — 'concrete'

    raw = [
        5   295 5   285 70  295 70  285;
        5   260 5   252 65  260 65  252;
        5   230 5   214 68  230 68  214;
        10  170 10  160 70  170 70  160;
        4   140 4   125 65  140 65  125;
        10  100 10  80  80  100 80  80;
        10  70  10  60  60  70  60  60;
        5   40  5   20  60  40  60  20;
        110 295 110 280 180 295 180 280;
        90  270 90  220 120 270 120 220;
        95  180 95  170 140 180 140 170;
        100 20  100 5   140 20  140 5;
        182 270 182 240 200 270 200 240;
        155 230 155 200 165 230 165 200;
        150 190 150 170 165 190 165 170;
        150 160 150 150 215 160 215 150;
        152 130 152 118 210 130 210 118;
        150 60  150 50  193 60  193 50;
        220 270 220 255 280 270 280 255;
        200 210 200 200 233 210 233 200;
        260 230 260 220 270 230 270 220;
        240 190 240 180 270 190 270 180;
        230 160 230 150 250 160 250 150;
        268 160 268 140 280 160 280 140;
        230 110 230 70  280 110 280 70;
        190 40  190 30  240 40  240 30;
        250 35  250 20  273 35  273 20;
        280 35  280 20  295 35  295 20
    ];

    N = size(raw, 1);
    Buildings = repmat(struct('xmin',0,'xmax',0,'ymin',0,'ymax',0,'material','concrete'), N, 1);
    for k = 1:N
        xs = raw(k, [1 3 5 7]);
        ys = raw(k, [2 4 6 8]);
        Buildings(k).xmin = min(xs);
        Buildings(k).xmax = max(xs);
        Buildings(k).ymin = min(ys);
        Buildings(k).ymax = max(ys);
        Buildings(k).material = 'concrete';
    end
end
